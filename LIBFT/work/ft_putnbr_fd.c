#include"libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	dig;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(fd, "-2147483648", 11);
		}
		else
		{
			ft_putchar_fd('-', fd);
			ft_putnbr_fd((-n), fd);
		}
	}
	else if (n >= 0 && n <= 9)
	{
		dig = (n + '0');
		ft_putchar_fd(dig, fd);
	}
	else
	{
		ft_putnbr_fd((n / 10), fd);
		ft_putnbr_fd((n % 10), fd);
	}
}
